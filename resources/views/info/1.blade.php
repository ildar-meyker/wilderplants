    <h3>Light Guide</h3>

    <h4>TO PICK THE RIGHT PLANTS YOU NEED TO KNOW THE LEVEL OF LIGHT IN YOUR ROOM.  </h4>

    <p>
        The amount of light your space receives depends on the direction of the window and how far from it the plant is. 
    </p>

    <img src="https://images.unsplash.com/photo-1442458017215-285b83f65851?ixid=MnwxMjA3fDB8MHxwaG90by1wYWdlfHx8fGVufDB8fHx8&ixlib=rb-1.2.1&auto=format&fit=crop&w=675&q=80" alt="">

    <p>
        First, using the compass or map on your phone, see what direction your window faces: North, East, or South, or West.
    </p>

    <p>
        Second think where in the room you would like to place a plant. 
    </p>

    <p>
        Depending on the distance from the window the spot will get different level of light: <b>low</b>, <b>medium</b> or <b>bright</b> (indirect or direct). 
    </p>

    <p>
        Use our filters to choose your plants according to your light level.
    </p>

